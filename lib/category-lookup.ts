/**
 * Normalizes an item name for lookup
 * - Lowercase
 * - Trim whitespace
 * - Remove common prefixes/suffixes
 * - Handle plurals
 */
export function normalizeForLookup(itemName: string): string {
  let normalized = itemName.toLowerCase().trim()

  // Remove quantity indicators at the start
  normalized = normalized.replace(/^\d+[\s]*(x\s*)?/i, '')

  // Remove common descriptive prefixes
  const prefixesToRemove = [
    'fresh ', 'frozen ', 'tinned ', 'canned ', 'dried ',
    'organic ', 'free range ', 'free-range ',
    'large ', 'medium ', 'small ', 'mini ',
    'whole ', 'sliced ', 'diced ', 'chopped ', 'minced ', 'grated ', 'shredded ',
    'raw ', 'cooked ', 'roasted ', 'toasted ',
    'boneless ', 'skinless ', 'boneless, skinless ', 'boneless skinless ',
    'lean ', 'extra lean ', 'low fat ', 'low-fat ', 'reduced fat ', 'fat free ', 'fat-free ',
    'unsalted ', 'salted ',
    'plain ', 'natural ',
    'ripe ',
  ]

  for (const prefix of prefixesToRemove) {
    if (normalized.startsWith(prefix)) {
      normalized = normalized.slice(prefix.length)
    }
  }

  // Remove common suffixes
  const suffixesToRemove = [
    ', diced', ', chopped', ', sliced', ', minced', ', grated', ', shredded',
    ', fresh', ', dried', ', frozen', ', canned', ', tinned',
    ', large', ', medium', ', small',
    ', ripe',
    ' (optional)', ' optional',
    ', to taste', ' to taste',
    ', for serving', ' for serving',
    ', for garnish', ' for garnish',
  ]

  for (const suffix of suffixesToRemove) {
    if (normalized.endsWith(suffix)) {
      normalized = normalized.slice(0, -suffix.length)
    }
  }

  // Remove parenthetical content
  normalized = normalized.replace(/\s*\([^)]*\)\s*/g, ' ')

  // Clean up multiple spaces
  normalized = normalized.replace(/\s+/g, ' ').trim()

  return normalized
}

/**
 * Pre-processes an item name for AI categorization
 * More aggressive cleaning for when item isn't in lookup
 */
export function preprocessForAI(itemName: string): string {
  let processed = normalizeForLookup(itemName)

  // Additional cleaning for AI
  // Remove brand names patterns
  processed = processed.replace(/\b(tesco|sainsbury|asda|morrisons|aldi|lidl|waitrose|m&s)\b/gi, '')

  // Remove size/quantity descriptors
  processed = processed.replace(/\b(\d+g|\d+ml|\d+kg|\d+l|\d+ pack|\d+-pack)\b/gi, '')

  // Clean up
  processed = processed.replace(/\s+/g, ' ').trim()

  return processed
}

/**
 * Looks up a category for an item
 * Returns the category name if found, null if not
 */
export function lookupCategory(itemName: string): string | null {
  const normalized = normalizeForLookup(itemName)

  // Direct lookup
  if (CATEGORY_LOOKUP[normalized]) {
    return CATEGORY_LOOKUP[normalized]
  }

  // Try without trailing 's' (simple plural)
  if (normalized.endsWith('s') && normalized.length > 3) {
    const singular = normalized.slice(0, -1)
    if (CATEGORY_LOOKUP[singular]) {
      return CATEGORY_LOOKUP[singular]
    }
  }

  // Try without trailing 'es'
  if (normalized.endsWith('es') && normalized.length > 4) {
    const singular = normalized.slice(0, -2)
    if (CATEGORY_LOOKUP[singular]) {
      return CATEGORY_LOOKUP[singular]
    }
  }

  // Try adding 's' (in case lookup has plural form)
  const plural = normalized + 's'
  if (CATEGORY_LOOKUP[plural]) {
    return CATEGORY_LOOKUP[plural]
  }

  return null
}

