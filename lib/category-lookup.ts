/**
 * Validates and potentially corrects a category assignment
 * Returns the corrected category if a mismatch is found
 */
export function validateCategory(itemName: string, suggestedCategory: string): string {
  const lowerItem = itemName.toLowerCase()

  // Check each category's validation rules
  for (const [category, rules] of Object.entries(VALIDATION_KEYWORDS)) {
    // Check if item matches this category's keywords
    const matchesKeyword = rules.keywords.some(kw => lowerItem.includes(kw))
    const matchesNotKeyword = rules.notKeywords?.some(kw => lowerItem.includes(kw)) ?? false

    if (matchesKeyword && !matchesNotKeyword) {
      // Item should be in this category
      if (suggestedCategory !== category) {
        // Suggested category is wrong, correct it
        return category
      }
    }
  }

  // No correction needed
  return suggestedCategory
}
